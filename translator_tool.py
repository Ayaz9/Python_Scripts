'''
This script translates the user entered word to polish in our test, and sounds the translated word. 
We can of course change the user input language and translated language. 
'''

from googletrans import Translator
from playsound import playsound
from gtts import gTTS 

translator=Translator()

while True:
    user_input = input('What to translate?: \n')
    translated_text= translator.translate(user_input, src='en', dest='pl').text
    print(translated_text)
    speak = gTTS(text=translated_text, lang='pl', slow=False)
    speak.save("captured_voice.mp3")
    playsound('captured_voice.mp3')


